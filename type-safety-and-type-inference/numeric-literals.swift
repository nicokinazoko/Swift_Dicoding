//numeric literal merupakan angka yang didapat dari berbagai bentuk
//misal dari desimal, biner, dsb

//Angka desimal, tanpa awalan.
//Angka biner, dengan awalan 0b.
//Angka oktal, dengan awalan 0o.
//Angka heksadesimal, dengan awalan 0x.
//contoh
let decimalInteger      =   18
let binaryInteger       =   0b10010
let octalInteger        =   0o22
let hexadecimalInteger  =   0x12

//contoh angka dengan eksponen
let decimalExponen      =   1.25e2
let anotherDecimalEx    =   1.25e-2

print(decimalInteger, binaryInteger, octalInteger, hexadecimalInteger, decimalExponen, anotherDecimalEx)
//output : 18 18 18 18 125.0 0.0125  